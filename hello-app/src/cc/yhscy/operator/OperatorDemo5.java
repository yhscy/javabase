public class OperatorDemo5 {
    //目标：掌握基本赋值运算符

    public static void main(String[] args) {
        int a = 2;
        int b = 1;

        System.out.println(a += b);
        System.out.println(a -= b);
        System.out.println(a *= b);
        System.out.println(a /= b);
        System.out.println(a %= b);

    }
}
